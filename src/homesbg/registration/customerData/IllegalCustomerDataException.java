package homesbg.registration.customerData;

public class IllegalCustomerDataException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = -204990196368683593L;

	public IllegalCustomerDataException() {
		super();
	}

	public IllegalCustomerDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public IllegalCustomerDataException(String message) {
		super(message);
	}

	public IllegalCustomerDataException(Throwable cause) {
		super(cause);
	}
	
	
	
}
