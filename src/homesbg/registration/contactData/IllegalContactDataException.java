package homesbg.registration.contactData;

public class IllegalContactDataException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6725363212550801001L;

	public IllegalContactDataException() {
		super();
	}

	public IllegalContactDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public IllegalContactDataException(String message) {
		super(message);
	}

	public IllegalContactDataException(Throwable cause) {
		super(cause);
	}
	
	
	
}
