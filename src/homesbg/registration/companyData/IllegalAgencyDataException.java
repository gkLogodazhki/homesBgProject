package homesbg.registration.companyData;

public class IllegalAgencyDataException extends Exception{

	/**
	 * 
	 */
	private static final long serialVersionUID = 7183031138146432654L;

	public IllegalAgencyDataException() {
		super();
	}

	public IllegalAgencyDataException(String message, Throwable cause) {
		super(message, cause);
	}

	public IllegalAgencyDataException(String message) {
		super(message);
	}

	public IllegalAgencyDataException(Throwable cause) {
		super(cause);
	}

	
}
